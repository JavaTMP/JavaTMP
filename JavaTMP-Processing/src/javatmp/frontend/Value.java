class Value {

    private String original;

    public Value() {

    }

    public Value(String original) {
        this.original = original;
    }

    /**
     * @return the original
     */
    public String getOriginal() {
        return original;
    }

    /**
     * @param original the original to set
     */
    public void setOriginal(String original) {
        this.original = original;
    }

}
